int		ft_max(int *tab, int length)
{
	int m;
	int i;

	m = tab[0];
	i = 1;
	while (i < length)
	{
		if (tab[i] > m)
		{
			m = tab[i];
			i++;
		}
		else
			i++;
	}
	return (m);
}
